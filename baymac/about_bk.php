            <strong>Who is Baymac?</strong><br /><br />
Baymac is an international management consultancy firm; specializing
in providing turn-key management solutions for companies including the
provision of advice on human asset remuneration and benefit packages. We
provide advice on the selection and recruitment of employees and source
and promote the most attractive employee benefits at the least cost to
employers.<br /><br />
Baymac, as the liaison between the insurance companies, brokers, employers
and employees, provides a seamless transaction process and beneficial
partnership. Our aim is to deliver exceptional results for our partners and
clients alike.<br /><br />
Baymac is not an insurance company or broker, nor does Baymac provide
insurance, financial or investment advice. Baymac assists companies and
employee groups or associations, access information on employment benefits
worldwide by introducing them to reputable brokers or service providers
who have a proven track record for delivering quality. Baymac utilises the
services of licensed brokers and entities, and brokers agreements and
arrangements between clients and benefit providers, and legal counsel where
required.<br /><br />
We believe that the provision of valuable benefits to employees at minimum
costs to employers facilitates a harmonious employer-employee relationship,
allowing employees to feel valued by their employer while creating employee
motivation, greater commitment and an increased overall productivity for the
Company.<br /><br />

